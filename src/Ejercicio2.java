    private void cmdCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cmdCalcularActionPerformed
      int nhijos;
        long sba, bono, total;
        String btotal, sfinal;

     if(txtbase.getText().trim().isEmpty()){
         JOptionPane.showMessageDialog(this, "Digite el sueldo ","error", JOptionPane.ERROR_MESSAGE);  
        txtbase.requestFocusInWindow();
        txtbase.selectAll();
     }  
     else if(txtcantidad.getText().trim().isEmpty()){
        JOptionPane.showMessageDialog(this, "Digite n√∫mero de hijos","error", JOptionPane.ERROR_MESSAGE);  
        txtcantidad.requestFocusInWindow(); 
         txtcantidad.selectAll();    
     }
     else{
     sba=Long.parseLong(txtbase.getText());
     nhijos=Integer.parseInt(txtcantidad.getText());
     
     if(nhijos==0){  
     }
    bono = nhijos*80000;
    total = bono+sba;
    
    btotal=String.valueOf(bono);
    txtBonos.setText (btotal);    
    
    sfinal=String.valueOf(total);
    txtFinal.setText (sfinal);      
         
     }
    }//GEN-LAST:event_cmdCalcularActionPerformed

